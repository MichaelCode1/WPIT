"""
environment_mod.dB_ds

**Description**:
_____________________________________________________________________________________________________________________

Routine to calculate the gradient of the magnetic field strength with respect to the distance along the magnetic field line

_____________________________________________________________________________________________________________________
_____________________________________________________________________________________________________________________

**Inputs**:
_____________________________________________________________________________________________________________________

B_arg: dipole geomagnetic field in T

lamda_arg: magnetic latitude in rad

L_arg: L shell
_______________________________________________________________________________________________________________________
_______________________________________________________________________________________________________________________

**Outputs**:
_____________________________________________________________________________________________________________________

dB_ds_arg: gradient of geomagnetic field along the field line
_____________________________________________________________________________________________________________________
________________________________________________________________________________________________________________________

**Reference**:
_____________________________________________________________________________________________________________________

_____________________________________________________________________________________________________________________
________________________________________________________________________________________________________________________

"""

import numpy as np
from environment_mod import const


def dB_ds(B_arg,lamda_arg,L_arg):

    slat = np.sin(lamda_arg)
    clat = np.cos(lamda_arg)
    slat_term = np.sqrt(1 + 3*slat*slat)
    dB_ds_arg = (3.0*B_arg/(L_arg*const.Re)) *(slat/slat_term) * (1.0/(slat_term*slat_term) + 2.0/(clat*clat))

    return dB_ds_arg