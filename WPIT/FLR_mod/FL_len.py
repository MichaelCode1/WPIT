import numpy as np
from WPIT.Environment_mod import const

def FL_len(L, s): #len
        
    term1 = L*const.Re/2 * s*(1+3*s**2)**(1/2)
    term2 = np.log((3)**(1/2)*s+(1+3*s**2)**(1/2))/(2*(3)**(1/2)) * L*const.Re
    
    return (term1 + term2)