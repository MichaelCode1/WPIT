"""
waveproperties_mod.wave_packet_gauss

**Description**:
_____________________________________________________________________________________________________________________

Simulate a static, monochromatic gaussian wave packet
_____________________________________________________________________________________________________________________
_____________________________________________________________________________________________________________________

**Inputs**:
_____________________________________________________________________________________________________________________

Bw0_arg: initial wave B field magnitude

lamda_arg: latitude in rad

lamdaw_arg: latitudinal range

_______________________________________________________________________________________________
_______________________________________________________________________________________________________________________

**Outputs**:
_____________________________________________________________________________________________________________________

Bwave: wave B field magnitude
_____________________________________________________________________________________________________________________
________________________________________________________________________________________________________________________

**Reference**:
_____________________________________________________________________________________________________________________
_________________________________________________________________________________________________
________________________________________________________________________________________________________________________

"""

import numpy as np


def wave_packet_gauss(Bw0_arg,lamda_arg,lamda_range):
    Bwave=Bw0_arg*np.exp(-lamda_arg**2/lamda_range**2)

    return Bwave