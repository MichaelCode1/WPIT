"""
environment_mod.debye_length

**Description**:
_____________________________________________________________________________________________________________________

Routine to calculate the debye length

_____________________________________________________________________________________________________________________
_____________________________________________________________________________________________________________________

**Inputs**:
_____________________________________________________________________________________________________________________

ne_arg: electron density in m^-3

Te_arg: electron temperature in K
_______________________________________________________________________________________________________________________
_______________________________________________________________________________________________________________________

**Outputs**:
_____________________________________________________________________________________________________________________

debye: Debye length in m

_____________________________________________________________________________________________________________________
________________________________________________________________________________________________________________________

**Reference**:

_____________________________________________________________________________________________________________________
Parks G K 1991 Physics of Space Plasmas. An Introduction 1st edn (Redwood City, CA: Addison Wesley), p. 22
_____________________________________________________________________________________________________________________
________________________________________________________________________________________________________________________

"""

import numpy as np
from WPIT.Environment_mod import const

def debye_length(ne_arg,Te_arg):
    debye_fac_1=const.epsilon0*const.kb*Te_arg
    debye_fac_2=ne_arg*const.qe*const.qe
    debye=debye_fac_1/debye_fac_2

    return debye
    