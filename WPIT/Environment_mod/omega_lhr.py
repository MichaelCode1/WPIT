"""
environment_mod.omega_lhr

**Description**:
_____________________________________________________________________________________________________________________

Routine to calculate the gyrofrequency of a particle
_____________________________________________________________________________________________________________________
_____________________________________________________________________________________________________________________

**Inputs**:
_____________________________________________________________________________________________________________________

wce_arg: electron gyrofrequency

wpe_arg: electron plasma frequency

wci_arg: ion gyrofrequency

wpi_arg: ion plasma frequency 

_______________________________________________________________________________________________
_______________________________________________________________________________________________________________________

**Outputs**:
_____________________________________________________________________________________________________________________

wlh_tmp: lower hybrid resonance frequency
_____________________________________________________________________________________________________________________
________________________________________________________________________________________________________________________

**Reference**:
_____________________________________________________________________________________________________________________
Parks, G. K. (1991). Physics of space plasmas. An introduction.
_____________________________________________________________________________________________________________________
________________________________________________________________________________________________________________________

"""

import numpy as np

def omega_lhr(wce_arg,wpe_arg,wci_arg,wpi_arg):
    fac1=1/(wci_arg*wce_arg)
    fac2=1/(wpe_arg**2+wpi_arg**2)
    sqrtfac=np.sqrt(fac1+fac2)
    wlh_tmp=1/sqrtfac
    return wlh_tmp